function GlacierPkGetReaperPlayer()
    local glacierpk_kills = PANEL_PLAYER_KILL_COUNT["glacierpk"]
    if not glacierpk_kills then
        return "N/A", 0
    end

    local top_player_name = "N/A"
    local most_kills = 0

    for player_name, kills in pairs(glacierpk_kills) do
        if kills > most_kills then
            most_kills = kills
            top_player_name = player_name
        end
    end

    return top_player_name, most_kills
end
